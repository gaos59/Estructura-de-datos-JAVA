    private void SeleccionProductora(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SeleccionProductora
        try {
            Productora productoraSelec = obtenerValorSeleccionadoProductora(productoras);
            RefrescarPeliculas(productoraSelec.getPeliculas());
        }
        catch (Exception e){
            limpiarPeliculas();
            JOptionPane.showMessageDialog(this, "No se puede mostar películas", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SeleccionProductora

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        try {
            Productora productoraSelec = obtenerValorSeleccionadoProductora(productoras);
            productoraSelec.setPeliculas(EliminarObjeto.EliminarObjeto(productoraSelec.getPeliculas()));
            if (productoraSelec.getPeliculas() != null && productoraSelec.getPeliculas().length != 0){
               RefrescarPeliculas(productoraSelec.getPeliculas()); 
            }
            else {
                limpiarPeliculas();
            }
            RefrescarProductoras(productoras);
        }
        catch (Negocio.ExcepcionPersonalizada e) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar películas: " + e.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this, "No se elimino la pelicula por un error verifique que selecciono alguna productora", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

