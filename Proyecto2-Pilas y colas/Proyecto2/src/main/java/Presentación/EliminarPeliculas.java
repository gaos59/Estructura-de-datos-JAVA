    public void RefrescarProductoras (Productora[] productoras) throws Negocio.ExcepcionPersonalizada{
        Productora[] productorasOrigen;
        int ProductorasNoVacias = 0;
        for (Productora productora : productoras) {
            if (productora.getPeliculas() != null) {
                ProductorasNoVacias++;
            }
        }
        if (ProductorasNoVacias > 0){
            productorasOrigen = new Productora[ProductorasNoVacias];
            int contador = 0;
            for (Productora productora : productoras) {
                if (productora.getPeliculas() != null) {
                    productorasOrigen[contador] = productora;
                    contador++;
                }
            }
         
            try{
                tableModelProductora = new ProductoraTableModel(productorasOrigen);
                TblProductoras.setModel(tableModelProductora);
            }
            catch (Negocio.ExcepcionPersonalizada e) {
                JOptionPane.showMessageDialog(this, "No se puede abrir opción de registrar películas: " + e.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(this, "No se puede actualizar la tabla de productoras", "Error", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }
    private Productora obtenerValorSeleccionadoProductora(Productora[] productorasSelect) throws java.lang.Exception {
        int selectedRow = TblProductoras.getSelectedRow();

        try {
            int selectedValue = Integer.parseInt(String.valueOf(TblProductoras.getValueAt(selectedRow, 0)));
            return productorasSelect[selectedValue-1];            
        }
        catch (NumberFormatException e) {
            throw Exception (e.getMessage());
        }
    } 

