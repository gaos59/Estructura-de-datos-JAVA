    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        
        try {
            int id = Integer.parseInt(TxfBuscar.getText());
            
            Impresora impresora = menuPrincipal.buscarImpresora(id);
        
            TxpResultado.setText("ID: " + impresora.getId() + "\nTipo: " + impresora.getTipo() + "\nMarca: " + impresora.getMarca() + "\nPeso: " + impresora.getPeso());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede buscar impresora verifique el ID brindado", "Error", JOptionPane.ERROR_MESSAGE);
            TxfBuscar.requestFocusInWindow();
        }        
    }//GEN-LAST:event_BtnBuscarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        try {
            int id = Integer.parseInt(TxfBuscar.getText());
            
            menuPrincipal.eliminarImpresora(id);
            this.dispose();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede buscar impresora verifique el ID brindado", "Error", JOptionPane.ERROR_MESSAGE);
            TxfBuscar.requestFocusInWindow();
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed


